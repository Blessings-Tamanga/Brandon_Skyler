drop policy if exists "acting_all" on public."actingProjects";
create policy "acting_all"
on public."actingProjects"
for all
to anon, authenticated
using (true)
with check (true);

drop policy if exists "gallery_all" on public."galleryItems";
create policy "gallery_all"
on public."galleryItems"
for all
to anon, authenticated
using (true)
with check (true);

drop policy if exists "team_all" on public."teamMembers";
create policy "team_all"
on public."teamMembers"
for all
to anon, authenticated
using (true)
with check (true);

drop policy if exists "contact_all" on public."contactMessages";
create policy "contact_all"
on public."contactMessages"
for all
to anon, authenticated
using (true)
with check (true);

commit;
